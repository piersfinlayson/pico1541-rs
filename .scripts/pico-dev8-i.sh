#!/bin/bash

# ATN low, 0x20 listen | 0x08 device, 0x60 | 0x0f secondary address 15
usbcmd/usbcmd.py -v 0x1209 -p 0xf541 bulk out 0x01 -d 0x09120200
usbcmd/usbcmd.py -v 0x1209 -p 0xf541 bulk out 0x01 -d 0x286f
usbcmd/usbcmd.py -v 0x1209 -p 0xf541 bulk in 0x81 -l 3

# ATN high, 0x49, ASCII I for initialize
usbcmd/usbcmd.py -v 0x1209 -p 0xf541 bulk out 0x01 -d 0x09100100
usbcmd/usbcmd.py -v 0x1209 -p 0xf541 bulk out 0x01 -d 0x49
usbcmd/usbcmd.py -v 0x1209 -p 0xf541 bulk in 0x81 -l 3

# ATN low, 0x3f, unlisten
usbcmd/usbcmd.py -v 0x1209 -p 0xf541 bulk out 0x01 -d 0x09120100
usbcmd/usbcmd.py -v 0x1209 -p 0xf541 bulk out 0x01 -d 0x3f
usbcmd/usbcmd.py -v 0x1209 -p 0xf541 bulk in 0x81 -l 3
